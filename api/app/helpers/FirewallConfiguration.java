package helpers;

import java.lang.reflect.Type;
import java.net.MalformedURLException;
import java.net.URL;
import java.util.ArrayList;
import java.util.LinkedList;
import java.util.List;

import javax.net.ssl.HostnameVerifier;
import javax.net.ssl.HttpsURLConnection;
import javax.net.ssl.SSLContext;
import javax.net.ssl.SSLSession;
import javax.net.ssl.TrustManager;
import javax.net.ssl.X509TrustManager;
import org.apache.commons.io.IOUtils;

import com.google.gson.Gson;
import com.google.gson.JsonArray;

public class FirewallConfiguration {

	private static final String FIREWALL_IPS = "http://docs.mashape.com/firewall-ips.json";
	private static List<String> ips;
	
	static {
		// Create a trust manager that does not validate certificate chains
		TrustManager[] trustAllCerts = new TrustManager[]{
		    new X509TrustManager() {
		        public java.security.cert.X509Certificate[] getAcceptedIssuers() {
		            return null;
		        }
		        public void checkClientTrusted(
		            java.security.cert.X509Certificate[] certs, String authType) {
		        }
		        public void checkServerTrusted(
		            java.security.cert.X509Certificate[] certs, String authType) {
		        }
		    }
		};

		// Install the all-trusting trust manager
		try {
		    SSLContext sc = SSLContext.getInstance("SSL");
		    sc.init(null, trustAllCerts, new java.security.SecureRandom());
		    HttpsURLConnection.setDefaultSSLSocketFactory(sc.getSocketFactory());
		} catch (Exception e) {
		}
		
		try {
			URL	url = new URL(FIREWALL_IPS);
			Gson gson = new Gson();
			ips = new ArrayList<String>();
			ips = gson.fromJson(IOUtils.toString(url.openStream()), List.class);
			System.out.println(ips);
		} catch (Exception e) {
			throw new RuntimeException(e);
		}
	}
	
	public static boolean isAuthorized(String ip) {
		return ips.contains(ip.trim());
	}
}
